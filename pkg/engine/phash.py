from sys import version_info as _swig_python_version_info
if _swig_python_version_info >= (2, 7, 0):
    def swig_import_helper():
        import importlib
        pkg = __name__.rpartition('.')[0]
        mname = '.'.join((pkg, '_phash')).lstrip('.')
        try:
            return importlib.import_module(mname)
        except ImportError:
            return importlib.import_module('_phash')
    _phash = swig_import_helper()
    del swig_import_helper
elif _swig_python_version_info >= (2, 6, 0):
    def swig_import_helper():
        from os.path import dirname
        import imp
        fp = None
        try:
            fp, pathname, description = imp.find_module('_phash', [dirname(__file__)])
        except ImportError:
            import _phash
            return _phash
        try:
            _mod = imp.load_module('_phash', fp, pathname, description)
        finally:
            if fp is not None:
                fp.close()
        return _mod
    _phash = swig_import_helper()
    del swig_import_helper
else:
    import _phash
del _swig_python_version_info

